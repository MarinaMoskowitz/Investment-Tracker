SET sql_mode="";
CREATE DATABASE IF NOT EXISTS `stocktransactions`;
USE `stocktransactions`;

DROP TABLE IF EXISTS customer;
CREATE TABLE customer (
    customer_id int(20) NOT NULL auto_increment,
    customer_name VARCHAR(20) NOT NULL,
    PRIMARY KEY (customer_id)
);

DROP TABLE IF EXISTS company_stock;
CREATE TABLE company_stock (
    stock_symbol VARCHAR(10),
    PRIMARY KEY (stock_symbol)
);

#COMPANIES_IN_PROTFOLIO ENUM is an enum of company_stock_symbols
DROP TABLE IF EXISTS portfolio;
CREATE TABLE portfolio (
    customer_id INT(20) NOT NULL,
    portfolio_id INT(20) AUTO_INCREMENT NOT NULL,
    PRIMARY KEY (portfolio_id),
    KEY customer_id_fk (customer_id),
    CONSTRAINT customer_id_fk FOREIGN KEY (customer_id)
        REFERENCES customer (customer_id)
        ON UPDATE CASCADE ON DELETE RESTRICT
);

DROP TABLE IF EXISTS stock_transaction;
CREATE TABLE stock_transaction (
    portfolio_id INT(20) NOT NULL,
    transaction_date DATETIME,
    stock_symbol VARCHAR(10) NOT NULL,
    open_price_at_transaction DECIMAL(8 , 2 ) DEFAULT NULL,
    transaction_type ENUM('buy', 'sell'),
    transaction_volume INT(10) NOT NULL,
    PRIMARY KEY (portfolio_id , transaction_date , stock_symbol , transaction_type , transaction_volume),
    KEY portfolio_id_fk (portfolio_id),
    KEY stock_symbol_fk (stock_symbol),
    CONSTRAINT portfolio_id_fk FOREIGN KEY (portfolio_id)
        REFERENCES portfolio (portfolio_id)
        ON UPDATE RESTRICT ON DELETE NO ACTION,
    CONSTRAINT stock_symbol_fk FOREIGN KEY (stock_symbol)
        REFERENCES company_stock (stock_symbol)
        ON UPDATE RESTRICT ON DELETE NO ACTION
);

# if stock cannot be bought or sold, make curent_price default null perhaps?
DROP TABLE IF EXISTS daily_stock_price;
CREATE TABLE daily_stock_price (
	ccurrent_date DATETIME ,
    stock_symbol VARCHAR(10),
    current_price DECIMAL(8, 2) DEFAULT NULL,
    PRIMARY KEY (ccurrent_date, stock_symbol),
	KEY stock_symbol_fk (stock_symbol),
    CONSTRAINT stock_symbol_price_fk FOREIGN KEY (stock_symbol)
        REFERENCES company_stock (stock_symbol)
        ON DELETE NO ACTION ON UPDATE NO ACTION
);


